package com.whale.library.data;

public class CustomerData {
	
	private int pk;
	
	private String id;

	private String pw;

	public int getPk() {
		return pk;
	}

	public void setPk(final int pk) {
		this.pk = pk;
	}

	public String getId() {
		return id;
	}

	public void setId(final String id) {
		this.id = id;
	}

	public String getPw() {
		return pw;
	}

	public void setPw(final String pw) {
		this.pw = pw; 
	}

}
